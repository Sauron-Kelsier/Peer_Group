    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
       
            // TODO add your handling code here:
            
            String roll=new String(rollNumberField.getText());
            String name=new String(nameField.getText());
            String standard=new String(standardField.getSelectedItem().toString());
            String[] sub=name.split(" ",2);
            String word=sub[0];
            System.out.println(word);
            String password=new String(word+""+roll);
            System.out.println(password);
            //nameField.setText(String.valueOf(password));
            if(roll.equals("") || name.equals("") || standard.equals(""))
            {
                JOptionPane.showMessageDialog(this,"Please enter all the fields");

            }
            else
            {
                try
                {
                    Statement start=(Statement)databaseConnection.createStatement();
                    String query=new String("INSERT INTO student_info (roll_number,name,password,std,questions_attempted,correct_answers) VALUES ('"+roll+"','"+name+"','"+password+"','"+standard+"','0','0')");
                    int res=start.executeUpdate(query);
                    if(res==0)
                    {
                        System.out.println("Insertion has failed");
                    }
                    else
                        System.out.println("Insertion is successful");
                }
                catch(MySQLIntegrityConstraintViolationException e)
                {
                    JOptionPane.showMessageDialog(this,"Duplicate entries. Please change your values");
                    clearButtonActionPerformed(evt);

                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(this,"Insertioni not successful. Please try again");
                    clearButtonActionPerformed(evt);
                }
            }
            // auto password-assignment      
        
    }//GEN-LAST:event_addButtonActionPerformed

